function [ CPIGMX ] = CPIGMX_CORRE( ASTMD86,SG )
%CPIGMX_CORRE是利用ASTMD86及比重两个条件来计算石油馏分的理想气体热容的方法；
%   ASTMD86需要T10 T30 T50 T70 T90这五个数据，单位为F
WAT=WAT_CORRE( ASTMD86,SG );
OMEGA=OMEGA_CORRE( ASTMD86,SG );
MW=MW_CORRE( ASTMD86,SG );
A0=-1.41779+0.11828*WAT;
A1=-(6.99724-8.69326*WAT+0.27715*WAT^2)*10^(-4);
A2=-2.2582*10^(-6);
B0=1.09223-2.48245*OMEGA;
B1=-(3.434-7.14*OMEGA)*10^(-3);
B2=-(7.2661-9.2561*OMEGA)*10^(-7);
C=((12.8-WAT)*(10-WAT)/(10*OMEGA))^(2);
T=(60+459.67)/1.8;
CPIGMX=(A0+A1*T+A2*T^2-C*(B0+B1*T+B2*T^2));

end

