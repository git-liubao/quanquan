function [ KMX ] = KMX_CORRE( ASTMD86,SG )
%KMX_CORRE是通过ASTMD86和SG来计算热导率的函数
%   ASTMD86需要T10 T30 T50 T70 T90这五个数据，单位为F
MeABP=MeABP_CORRE( ASTMD86 );%计算出温度单位为F
Tb=(MeABP-32)*5/9+273.15;%将温度单位由F转化为K
T=25+273.15;
KMX=10^(-2)*(0.11594*Tb^0.7534*SG^0.5478-2.2989*Tb^0.2983*SG^0.0094)*(1.8*T-460)/300+2.2989*10^(-2)*Tb^0.2983*SG^0.0094;

end

