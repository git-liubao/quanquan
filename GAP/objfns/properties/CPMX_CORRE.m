function [ CPMX ] = CPMX_CORRE( ASTMD86,SG )
%CPMX_CORRE是通过ASTMD86和SG来计算Watson K的函数
%   ASTMD86需要T10 T30 T50 T70 T90这五个数据，单位为F

T=60+459.67;
WAT=WAT_CORRE( ASTMD86,SG );
A1=-1.17126+(0.023722+0.024907*SG)*WAT+(1.14982-0.046535*WAT)/SG;
A2=(10^(-4))*(1.0+0.82463*WAT)*(1.12172-0.27634/SG);
A3=(-10^(-8))*(1+0.82463*WAT)*(2.907-0.70958/SG);
CPMX1=A1+A2*T+A3*T^2;%此处算出单位为Btu/(Ib*R)
CPMX=CPMX1*1.055/(0.45392/1.8);
end

